import datetime

# определяем таблицу для опорной даты
base_table = {
    0: 'Собака',
    1: 'Свинья',
    2: 'Крыса',
    3: 'Бык',
    4: 'Тигр',
    5: 'Кролик',
    6: 'Дракон',
    7: 'Змея',
    8: 'Лошадь',
    9: 'Коза',
    10: 'Обезьяна',
    11: 'Петух'
}

# получаем ввод от пользователя
day, month, year = map(int, input("Введите день, месяц и год (через пробел): ").split())

# вычисляем количество дней между опорной датой и введенной датой
delta_days = (datetime.date(year, month, day) - datetime.date(1975, 12, 30)).days

# определяем земную ветвь для введенной даты
earthly_branch = base_table.get(delta_days % 12)

# выводим результат
print(f"Земная ветвь дня: {earthly_branch}")
