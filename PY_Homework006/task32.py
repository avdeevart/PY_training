import random

# Получить длину списка от пользователя
length = int(input("Введите длину списка: "))

# Создать случайный список чисел указанной длины
random_list = [random.randint(-10, 10) for _ in range(length)]
print("Случайный список:", random_list)

# Получить минимальное и максимальное значения для диапазона
min_value = int(input("Введите минимальное значение: "))
max_value = int(input("Введите максимальное значение: "))

# Проверить, что максимальное значение больше или равно минимальному
if max_value < min_value:
    raise ValueError("Максимальное значение должно быть больше или равно минимальному")

# Найти элементы списка, которые соответствуют заданному диапазону
matching_indices = [i for i, x in enumerate(random_list) if min_value <= x <= max_value]

# Вывести итоговый список и соответствующие элементы
print(f"Диапазон значений: [{min_value}, {max_value}]")
print(f"Индексы соответствующих элементов: {matching_indices}")

