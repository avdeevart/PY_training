# Задача 32: Определить индексы элементов массива (списка, заданного рандомно, по длине списка),
# значения которых принадлежат заданному диапазону  (т.е. не меньше заданного минимума и не больше заданного максимума, 
# которые вводятся с консоли) список этих элементов массива вывести на консоль.
# ==============================================================================================================================
